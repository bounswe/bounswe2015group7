


public class MathematicalOperations {


    public int binaryPlus(int x,int  y){
        return x+y;
    }

    public int binaryMinus(int x, int y) {
        return x-y;
    }

    public int times(int x, int y){ // returns x*y;
    	return x*y;
    }
    public int divide (int x, int y){// returns x/y
        return x/y;
    }
}
